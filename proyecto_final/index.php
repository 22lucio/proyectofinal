 <h1 class="subtitulo">Argumento</h1>
 <p>Saint Seiya conocida como Los Caballeros del Zodiaco en España 
    y en el territorio hispanoamericano, es una serie de manga escrita e ilustrada por Masami Kurumada. 
    La serie se centra en un grupo de jóvenes guerreros denominados «caballeros» (o «santos»), cuyo
     protagonista principal es Seiya (caballero que porta la armadura de Pegaso). Estos guerreros luchan 
     del lado de la diosa griega Athena, reencarnada en la humana Saori Kido para proteger a la 
     humanidad de las fuerzas del mal que quieren dominar la Tierra. Para sus batallas, cada caballero 
     utiliza su energía interior ligada al universo (el «cosmos»), sus técnicas especiales de lucha,
y sus armaduras (originalmente llamada cloth), todas inspiradas en cada una de las 88 constelaciones.</p>
<h3 class="subtitulo">Armaduras</h3>
<p>Las armaduras además de brindar protección a su portador, 
también pueden potenciar el cosmos o retener los recuerdos y poder de sus previos portadores.</p>
<h4>Bronce</h4>
<p>Las armaduras de Bronce fueron creadas por la Diosa Athena para sus protectores de más baja categoría. 
    Forjó 48 armaduras de este tipo, y se tratan de las vestimentas más débiles de todas 
    (con contadas excepciones, como la poderosa armadura del Santo de Fénix). 
    La mayoría de estas armaduras pueden soportar temperaturas de hasta 150 grados celsius, 
    e impactos fuertes provenientes de un Caballero de Plata, o leves en el caso de un Caballero Dorado.</p>
<h4>Plata</h4>
<p>Las armaduras de Plata son más resistentes que las que utilizan los Santos de Bronce, 
    pero ciertamente menos poderosas que las que usan los Caballeros Dorados 
    (con la excepción de la que utiliza Orfeo de Lira, la cual es tan poderosa como la de cualquier
     Santo de Oro). Son usadas por la Segunda Orden de Caballeros de Athena, 
    Diosa que forjó solo 28 de ellas, y son capaces resistir fríos y calores extremos.</p>
<h4>Oro</h4>
<p>La Diosa Athena creó 12 de estas armaduras, y corresponden a los guerreros más poderosos
     de toda la serie. Son capaces de resistir casi cualquier tipo de ataque, 
     así como extremos fríos y calores. Estas vestimentas cuentan con consciencia propia, 
     y por ese motivo se les puede ver actuar por sí solas: el caso más conocido es
      el de la armadura de Sagitario</p>
